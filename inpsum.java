import java.util.*;
public class inpsum {
public static void main(String[] args) {
    Scanner sc = new Scanner(System.in);
    System.out.print("First number is:");
    int a = sc.nextInt();
    System.out.print("Second number is:");
    int b = sc.nextInt();
    System.out.println("Sum is:");
    int sum = a+b;
    System.out.println(sum);
}
    
}