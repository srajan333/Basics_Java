import java.util.*;
public class inputproduct {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        System.out.print("a=");
        int a = sc.nextInt();
        System.out.print("b=");
        int b = sc.nextInt();
        System.out.print("Product of a&b will be:");
        int product = a*b;
        System.out.println(product);
    }
}
